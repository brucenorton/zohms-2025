                <p class="lead"><cite>Montréal, Québec, Canada, November 8, 2017:  </cite> Zero-Ohm est fier de vous annoncer l’arrivée imminente de la nouvelle génération de son modèle breveté et récompensé, le MS-4R (deux entrées, deux sorties, monté sur rack).</p>
